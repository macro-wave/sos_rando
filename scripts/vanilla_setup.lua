function get_object(name)
  local object = Tracker:FindObjectForCode(name)
  if object then
    return object
  end
  print("error! get_object - not found:", name)
  return nil
end

function set_vanilla_captures()
    for location, item in pairs(vanilla_captures) do
        local location_object = get_object(location)
        local item_object = get_object(item)
        if location_object then
            if item_object then
                location_object.CapturedItem = item_object
            else
                location_object.CapturedItem = nil
            end
        end
    end
end
